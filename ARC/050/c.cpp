ll powMod(ll x, ll n,ll mo) {
	if (n == 0) return 1 % mo;
	ll val = powMod(x, n / 2,mo);
	val *= val;
	val %= mo;
	if (n % 2 == 1) val *= x;
	return val % mo;
}

//区間変更・区間和取得
struct S{
    long long value;
    ll size;
    ll same;
    ll fail = 0;
};
using F = struct{
    ll update;
    ll div;
};
const ll ID = 8e18;

S op(S a, S b){ 
    if (a.size == 0 || b.size == 0) return {a.value + b.value, a.size + b.size, 1, 0};
    else if(!a.same || !b.same || a.value/a.size != b.value/b.size) return {a.value + b.value, a.size + b.size, 0, 0};
    else return {a.value + b.value, a.size+b.size, 1, 0};
}
S e(){ return {0,0,1,0}; }
S mapping(F f, S x){
    if(x.same == 0 && f.div != ID){
        x.fail = 1;
        return x;
    }
    if(f.update != ID){
        x = {x.size*f.update, x.size, 1, 0};
    }
    if(f.div != ID){
        x = {x.value/x.size/f.div*x.size, x.size, 1, 0};
    }
    return x;
}
F composition(F f, F g){ 
    if(f.div == ID && f.update == ID) return g;
    else if(f.update != ID) return f;
    else if(g.update == ID && g.div == ID) return f;
    else return {g.update, g.div == ID? f.div: min((ll)IINF, f.div*g.div)};
}

F id(){ return {ID,ID}; }

